// array push pop

let fruits = ["apple", "mango", "grapes"];
console.log(fruits);
// push-- add from last of array
fruits.push("banana");
console.log(fruits);
// pop -remove from  last of array
fruits.pop();
console.log(fruits.pop());
console.log(fruits);

let poppedFruit = fruits.pop();
console.log(fruits);
console.log("popped fruit is", poppedFruit);
